let marks = 30;

if (80 <= marks && marks <= 100) {
  console.log("Student passed with A++ Grade");
} else if (70 <= marks && marks <= 79) {
  console.log("Student passed with A Grade");
} else if (60 <= marks && marks <= 69) {
  console.log("Student passed with B++ Grade");
} else if (50 <= marks && marks <= 59) {
  console.log("Student passed with B Grade");
} else if (40 <= marks && marks <= 49) {
  console.log("Student passed with C++ Grade");
} else if (30 <= marks && marks <= 39) {
  console.log("Student passed with C Grade");
} else {
  console.log("Student failed");
}

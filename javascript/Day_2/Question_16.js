let n1 = 21;
let n2 = 45;

let operator = "+";

if (operator == "+") {
  console.log(`adition of two value ${n1 + n2}`);
} else if (operator == "-") {
  console.log(`substraction of two value ${n1 - n2}`);
} else if (operator == "*") {
  console.log(`multiplication of two value ${n1 * n2}`);
} else if (operator == "/") {
  console.log(`division of two value ${n1 / n2}`);
} else {
  console.log(`please add valid operator`);
}

// Logical AND, OR, NOT

#include<iostream>
using namespace std;

#define END endl << endl;

int main(){
    int a = 9, b = 10;

    cout << "a == b:  " << (a == b) << END;
    cout << "a != b:  " << (a != b) << END;
    cout << "a > b:  " << (a > b) << END;
    cout << "a < b:  " << (a < b) << END;

    return 0;
}




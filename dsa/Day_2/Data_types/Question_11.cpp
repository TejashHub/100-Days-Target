#include<iostream>
using namespace std;

int main(){
    cout << "size of a integer: " << sizeof(int) << endl;
    cout << "size of a string: " << sizeof(string) << endl;
    cout << "size of a float: " << sizeof(float) << endl;
    cout << "size of a double: " << sizeof(double) << endl;
    cout << "size of a logn: " << sizeof(long) << endl;
    cout << "size of a char: " << sizeof(char) << endl;
    return 0;
}